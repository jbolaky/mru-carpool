public class EmailAclException extends Exception {

	private static final long serialVersionUID = 4287789184843385983L;

	public EmailAclException() {
		super();
	}

	public EmailAclException(String message, Throwable cause) {
		super(message, cause);
	}

	public EmailAclException(String message) {
		super(message);
	}

	public EmailAclException(Throwable cause) {
		super(cause);
	}

}
